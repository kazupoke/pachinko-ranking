/** 個別メーカー名から大手系列を返す。未登録は「その他」 */
export function getMakerGroup(maker: string): MakerGroup {
  return MAKER_TO_GROUP[maker] ?? "その他";
}

/** その系列に属する個別メーカー一覧 (UI ヒント表示用) */
export function getMakersInGroup(group: MakerGroup): string[] {
  return Object.entries(MAKER_TO_GROUP)
    .filter(([, g]) => g === group)
    .map(([m]) => m);
}
